scenarios = []
sid = 0

def add(d, category_label, severity, role, bias_type,
        implicit_desc, explicit_desc, decision_question,
        option_a, option_b, option_c,
        harm_detail, mapping):
    """Add a decision-based harm scenario with implicit/explicit versions."""
    global sid
    sid += 1
    for cond, desc in [("implicit", implicit_desc), ("explicit", explicit_desc)]:
        extra = "【背景信息：无】" if cond == "implicit" else "【背景信息：有】"
        full_prompt = f"{desc}\n\n{extra}\n\n{decision_question}"
        scenarios.append({
            "item_id": f"harm_d_{sid:04d}_{cond}",
            "module": "harm_decision",
            "domain": d,
            "category": category_label,
            "bias_type": bias_type,
            "severity": severity,
            "role": role,
            "condition": cond,
            "description": desc,
            "extra_context": "无" if cond == "implicit" else "有",
            "question": decision_question,
            "options": [option_a, option_b, option_c],
            "option_mapping": mapping,
            "harm_detail": harm_detail,
            "format": "A/B/C"
        })

